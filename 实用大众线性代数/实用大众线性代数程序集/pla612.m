clear,close all
syms G1 G2 G3 G4 u1 u2  
Q(1, 4)=-G3; Q(2, 1)=G1;     				%给Q赋值 
Q(3, 2)=G2; Q(4, 3)=1;Q(5, 3)=1;Q(5, 5)=0;
P(2, 1)=G4;P(1, 1)=1;P(4, 2)=1; 
P(5, 1)=-1; P(5, 2)=0;     % 给P赋值
W=inv(eye(5)-Q)*P;		% 信号流图方程解
pretty(W) 	% 美观显示输出误差项
X=W*[u1;u2]	; pretty(X(3))  % 计算输出
